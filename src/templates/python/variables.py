# Variables y Tipos en Python 3.12
# Demostración de diferentes tipos de datos

# Números enteros
edad = 25
print(f"Edad: {edad}")

# Números flotantes
altura = 1.75
print(f"Altura: {altura} metros")

# Strings (cadenas de texto)
nombre = "Python"
mensaje = f"¡Hola, {nombre}!"
print(mensaje)

# Booleanos
es_activo = True
print(f"Estado activo: {es_activo}")

# None (equivalente a null)
valor_nulo = None
print(f"Valor nulo: {valor_nulo}")

